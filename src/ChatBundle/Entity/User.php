     * Set picture
     *
     * @param string $picture
     *
     * @return User
     */



    /**
     * Set users
     *
     * @param string $users
     *
     * @return User
     */
    public function setUsers($users)
    {
        $this->users = $users;

        return $this;
    }

    /**
     * Get users
     *
     * @return string
     */
    public function getUsers()
    {
        return $this->users;
    }
}
